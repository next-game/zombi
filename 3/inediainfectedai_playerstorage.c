	void RefreshData()
	{
		if (!m_StorageFilePath) {
			return;
		}
		
		if (m_Ctx.IsOpen()) {
			m_Ctx.Close();
		}
		
		if (m_Ctx.Open(m_StorageFilePath, FileMode.READ)) {
			m_Ctx.Read(m_FloatData);
			m_Ctx.Close();
		}
	}
	
	void CommitData()
	{
		if (!m_StorageFilePath) {
			return;
		}
		
		if (m_Ctx.IsOpen()) {
			m_Ctx.Close();
		}
		
		if (m_Ctx.Open(m_StorageFilePath, FileMode.WRITE)) {
			m_Ctx.Write(m_FloatData);
			m_Ctx.Close();
		}
	}
	
	float Get(string key)
	{
		return m_FloatData.Get(key);
	}
	
	void Set(string key, float value)
	{
		m_FloatData.Set(key, value);
	}

