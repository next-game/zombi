class InediaInfectedAI_Smell
{
    PlayerBase m_Player;
    vector m_Position;
	float m_Radius;
    int m_CreatedTime;

    void InediaInfectedAI_Smell(PlayerBase pPlayer)
    {
        m_Player = pPlayer;
        m_Position = pPlayer.GetPosition() + 1.7 * vector.Up;
		m_Radius = pPlayer.InediaInfectedAI_GetSmellRadius();
        m_CreatedTime = GetGame().GetTime();
    }
}