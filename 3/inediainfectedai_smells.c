	void TickScheduler(float pDt)
	{
		UpdateSmellsHandler(pDt);
	}

	float UpdateSmellsHandlerTimer = 0;
	void UpdateSmellsHandler(float pDt)
	{
		if (!m_InediaInfectedAI_Config.Players.SmellsHandlerIsActive) {
			return;
		}

		UpdateSmellsHandlerTimer -= pDt;
		if (UpdateSmellsHandlerTimer > 0) return;
		UpdateSmellsHandlerTimer = 10;

		if (m_Smells.Count() == 0) return;
		
		m_Smells_IsUpdating = true;

		int currentTime = GetGame().GetTime();
		vector windSpeedDirection = GetGame().GetWeather().GetWind();

		float smellLifetimeMilliseconds = m_InediaInfectedAI_Config.Players.SmellLifetimeSeconds * GetRainModifier() * GetSnowfallModifier() * GetFogModifier() * 1000;

		for (int index = m_Smells.Count() - 1; index >= 0; index--) {
			InediaInfectedAI_Smell currentSmell = m_Smells.Get(index);

			if (!currentSmell.m_Player || !currentSmell.m_Player.IsAlive() || currentSmell.m_Radius <= 0 || (currentTime - currentSmell.m_CreatedTime) > smellLifetimeMilliseconds) {
				// Remove expired smell
				m_Smells.Remove(index);
			} else {
				// Update smell position
				currentSmell.m_Position += windSpeedDirection;

				// Without this variable, we get access violation. I still don't understand why ¯\_(ツ)_/¯
				float surfaceY = GetGame().SurfaceRoadY(currentSmell.m_Position[0], currentSmell.m_Position[2]);
				currentSmell.m_Position = Vector(currentSmell.m_Position[0], surfaceY, currentSmell.m_Position[2]) + 1.7 * vector.Up;
			}
		}
		
		m_Smells_LastUpdateTime = GetGame().GetTime();
		m_Smells_IsUpdating = false;
	}
	
	bool IsUpdating()
	{
		return m_Smells_IsUpdating;
	}

	float GetRainModifier()
	{
		return 1 - GetGame().GetWeather().GetRain().GetActual();
	}

