    void InediaInfectedAI_BaseCommand(DayZInfected pInfected)
    {
        m_InediaInfectedAI_Infected = ZombieBase.Cast(pInfected);
		if (!m_InediaInfectedAI_Infected) {
			SetFlagFinished(true);
			return;
		}

		if (!m_InediaInfectedAI_Infected.GetInputController() || !m_InediaInfectedAI_Infected.m_InediaInfectedAI_InfectedAnimationST) {
			SetFlagFinished(true);
			return;
		}
    }

// ORIENTATION TO POSITION HANDLER ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    void InediaInfectedAI_OrientationToPositionHandler(float pDt)
    {
        if (!InediaInfectedAI_InOrientationToPosition()) {
            return;
        }

        // Get current zombie direction angle
        float zmbDirectionAngle = m_InediaInfectedAI_Infected.GetDirection().VectorToAngles()[0];
		
		float speedRotationAngleMultiplier = Math.Clamp(GetVelocity(m_InediaInfectedAI_Infected).Length() * m_InediaInfectedAI_Infected.InediaInfectedAI_GetSpeedMultiplier() / 2, 1, 5);
		
        // Get diff rel angle
        float diffDirectionAngle = Math.DiffAngle(m_InediaInfectedAI_InOrientationToAngle, zmbDirectionAngle);
        if (diffDirectionAngle >= -5 && diffDirectionAngle <= 5) {
			zmbDirectionAngle += diffDirectionAngle;
			m_InediaInfectedAI_InOrientationToPosition = false;
        } else if (diffDirectionAngle > 0) {
			zmbDirectionAngle += 5 + 25 * speedRotationAngleMultiplier * diffDirectionAngle / 180;
		} else {
			zmbDirectionAngle += -5 + 25 * speedRotationAngleMultiplier * diffDirectionAngle / 180;
		}
        
        m_InediaInfectedAI_Infected.SetOrientation(zmbDirectionAngle * vector.Aside);
    }

	float m_InediaInfectedAI_InOrientationToAngle = 0;
	bool m_InediaInfectedAI_InOrientationToPosition = false;
	void InediaInfectedAI_SetOrientationToPosition(vector position)
	{
		if (position != m_InediaInfectedAI_Infected.GetPosition()) {
			m_InediaInfectedAI_InOrientationToAngle = vector.Direction(m_InediaInfectedAI_Infected.GetPosition(), position).VectorToAngles()[0];
			m_InediaInfectedAI_InOrientationToPosition = true;
		} else {
			m_InediaInfectedAI_InOrientationToPosition = false;
		}
	}
	
	bool InediaInfectedAI_InOrientationToPosition()
	{
		return m_InediaInfectedAI_InOrientationToPosition;
	}
