    static vector GetPlayerHorseChestPosition(PlayerBase player)
    {
#ifdef Dayz_Horse
		if (InediaInfectedAI_DayzHorseCompatibility.IsPlayerRidingHorse(player)) {
			Horse_Base horse = player.GetHorseObject();
			if (horse) {
				vector horsePos = horse.GetPosition();
				vector horseDir = horse.GetDirection();
				return horsePos + 0.7 * horseDir + 1.5 * vector.Up;
			}
		}
#endif
		
		return vector.Zero;
    }
