    static bool IsEAIPlayerInCreatureFriendlyFaction(PlayerBase eAIPlayer, EntityAI creature)
    {
#ifdef EXPANSIONMODAI
		if (eAIPlayer && eAIPlayer.IsAI() && creature) {
			eAIGroup aiGroup = eAIPlayer.GetGroup();
			if (aiGroup) {
				eAIFaction aiFaction = aiGroup.GetFaction();
				if (aiFaction && aiFaction.IsFriendlyEntity(creature)) {
					return true;
				}
			}
		}
#endif

        return false;
    }

	// PlayerBase.InediaInfectedAI_EEHitByFromPlayerToPlayerDamage() damage handler for players is disabled for "DayZ-Expansion-AI" bots
	// that have modified damage multipliers in quests (Quests/Objectives/AICamp => DamageReceivedMultiplier != 1 OR DamageMultiplier != 1).
	// If this is not done, "InediaInfectedAI" and "DayZ-Expansion-AI" will control damage simultaneously,
	// causing a conflict that will result in killed bots not being counted in quests "DayZ-Expansion-Quests".
	static bool AllowsDisablingPlayerToPlayerDamageHandler(PlayerBase sourcePlayer, PlayerBase targetPlayer)
	{
#ifdef EXPANSIONMODAI
		if (!sourcePlayer || !targetPlayer) {
			return false;
		}

		if (sourcePlayer.IsAI()) {
			eAIBase sourceAI = eAIBase.Cast(sourcePlayer);
			if (sourceAI && sourceAI.m_eAI_DamageMultiplier != 1) {
				return true;
			}
		}

		if (targetPlayer.IsAI()) {
			eAIBase targetAI = eAIBase.Cast(targetPlayer);
			if (targetAI && targetAI.m_eAI_DamageReceivedMultiplier != 1) {
				return true;
			}
		}
#endif

		return false;
	}
	
